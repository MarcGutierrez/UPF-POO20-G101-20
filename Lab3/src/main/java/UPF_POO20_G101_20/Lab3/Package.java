package UPF_POO20_G101_20.Lab3;

public class Package {
    private int width;
    private int height;

    public Package(int w, int h){
        width = w;
        height = h;
    }

	public int getWidth() {
		return width;
	}

	public void setWidth(int w) {
		width = w;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int h) {
		height = h;
	}

}
